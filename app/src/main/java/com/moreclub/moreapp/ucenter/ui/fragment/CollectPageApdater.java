package com.moreclub.moreapp.ucenter.ui.fragment;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

/**
 * Created by Captain on 2017/4/10.
 */

public class CollectPageApdater extends FragmentPagerAdapter {
    private List<Fragment> mFragments;

    public CollectPageApdater(FragmentManager fm, List<Fragment> fragments) {
        super(fm);
        mFragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return mFragments.get(position);
    }

    @Override
    public int getCount() {
        return mFragments.size();
    }
}
